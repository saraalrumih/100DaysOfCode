# Strings 2
greeting="  Welcome to 100DaysOfCode!     "
print(greeting)
print("The above statement with strip function: ",greeting.strip())
print("The length of the above statement is: ",len(greeting))
greeting=greeting.strip()
print("The above statement in lowercase letters: ",greeting.lower())
print("The above statement in uppercase letters: ",greeting.upper())
print("The above statement when we replace all (e)s with *: ",greeting.replace("e","*"))
print("The above statement splitted by spaces: ",greeting.split(" "))





